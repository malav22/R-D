          <ul>
           <p>1. <b>Quantitative analysis:</b> For Accurate Quantification of Chemical composition of any solid Material</p>
            <p> 2.<b>Qualitative analysis: </b> WDS Spectrum-To find out the elements present </p>
            <p>3.<b> BSE Imaging:</b> Phase Identification, Mineral Identification </p>
            <p>4. <b>SE Imaging</b> (Use: Topography)</p>
            <p>5. <b>Cathodoluminance Imaging </b>(Use: Reveals the defects and impurities in materials): </p>
            <p>6.<b>X-Ray Mapping:</b> Elemental distribution: With the help of X-Ray Mapping one can see the distribution the particular element in desired area of interest and segregation of impurities in metal alloy samples.</p>
            <p>7.<b>Mapping Quant: </b> The Mapping Quant option performs the true quantification of X-Ray Mappings. The results are quantified X ray maps displayed in weight %, Atomic% or Oxide%. After quantification, the measurement concentration at each pixel or for a set of pixels is available.</p>
            <p>8.<b>Geo Quant: Cation Calculation:</b> With the help of Geo quant Software it is easy to do the cation calculation. Software does it automatically after providing the kind the mineral. </p>
