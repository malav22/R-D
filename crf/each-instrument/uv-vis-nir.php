      <div class="details">
          <p>The Central Research Facility (CRF) has installed UV-VIS-NIR Spectrophotometer with attachments of Integrating Sphere and Peltier Accessory. The Integrating Sphere is for diffuse reflectance measurements of powder samples and the Peltier Accessory is for kinetics studies of liquid samples from room temperature to 80°C. The Colour Calculator Software is also installed with this equipment to calculate the Colour coordinates of the material.</p>
      </div>

      <div class="other-details">
          <h4>
            Instrument details :
          </h4>

            <ul>
              <p><b>Make & model : </b> Agilent Cary 5000</p>
              <p><b></b>Wavelength range : </b> 175 nm – 3300 nm </p>
              <p><b>Monochromator :</b>  Double out-of-plane Littrow Monochromator </p>
              <p><b>Light source :</b>  Tungsten-halogen and deuterium </p>
              <p><b>Detector : For UV-VIS :</b>  R 928 Photmultiplier; For NIR : Peltier cooled PbS photocell </p>
              <p><b>Integrating Sphere:</b>  110 mm module for Specular and Diffuse Reflectance measurements</p>
              <p><b>Slit width:</b>  Variable slit width down to 0.01 nm for optimum control over data resolution</p>
              <p><b>Sample compartment:</b>  Large size with Lock Down mechanism for quick changes and positioning</p>
              <p><b>Peltier Accessory:</b>  For kinetics studies of liquid samples</p>
              <p><b>Application software: </b> Windows based Cary WinUV software</p>
              <p><b>Colour Calculator software: </b> For measurements of colour coordinates</p>


            </ul>


