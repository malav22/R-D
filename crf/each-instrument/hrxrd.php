        <div class="details">
            <p>The Rigaku Smartlab is a multipurpose high resolution x-ray diffractometer (HRXRD) automated with guidance software (SmartLab Studio II). The XRD is equipped with a Monochromator in the diffracted beam side. The Converging Beam Optics (CBO) technology is used for fully automated beam alignment for various XRD applications, namely, powder diffraction, thin film metrology, SAXS (polymers and gels), in-plane scattering, micro diffraction, capillary measurement, texture and pole figure, X-ray reflectometry. SmartLab Studio II has an intelligent user guidance functionality that guides the operator through the intricacies of each XRD measurement. The software is also capable to perform automated phase/composition identification, qualitative and quantitative analysis, residual stress analysis, Rietveld analysis, thin-film analysis, etc.
            </p>

